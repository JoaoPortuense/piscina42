#include <unistd.h>

char	*ft_strupcase(char *str)
{
	int a;

	a = 0;
	while (str[a] != '\0')
	{
		if (str[a] >= 'a' && str[a] <= 'z')
		{
			str[a] -= 32;
			a++;
		}
		else
			a++;
	}
	return (str);
}
