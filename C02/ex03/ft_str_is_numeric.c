#include <unistd.h>

int		ft_str_is_numeric(char *str)
{
	int a;

	a = 0;
	if (str[0] == '\0')
	{
		return (1);
	}
	else
	{
		while (str[a] != '\0')
		{
			if (str[a] >= '0' && str[a] <= '9')
				a++;
			else
				return (0);
		}
	}
	return (1);
}
